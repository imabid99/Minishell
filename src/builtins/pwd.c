#include "minishell.h"

void	pwd_cmd(void)
{
	char	path[1024];

	if (!getcwd(path, 1024))
		g_glob.status = 0;
	g_glob.status = 0;
	ft_putstr_fd(path, 1);
	ft_putchar_fd('\n', 1);
}
